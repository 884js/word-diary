import { StyleSheet, Text, View } from 'react-native';
import { useColors } from '@/theme/ThemeContext';
import { spacing } from '@/theme/tokens';

/**
 * プロンプト（書く前の小さな誘い文句）の候補。
 * 起動ごとにランダムで1つ選ばれる。
 *
 * トーン方針:
 * - 命令でも問いかけでもなく、「そっと差し出す」温度感。
 * - アプリ側から話しかけるニュアンスは避ける（擬人化させない）。
 *   書き手の独り言・呟きとして読めるか、を選定基準にする。
 * - 句読点（、と。）のリズムで紙の手触りを保つ。
 * - 「書く促し」になっていないムードだけのものは入れない
 *   （例: "だいじょうぶ。" "ぬくもりの中で。" は除外している）。
 *
 * 中身は将来差し替え自由。string 配列なので、トーンや方向性を
 * 変えたいときは丸ごと差し替えてよい（キャラ化など）。
 */
const PROMPT_VARIATIONS = [
  // ベース「ひと言」表現
  '今日を、ひと言で。',
  '今日のひと言を。',
  '今日を、ことばに。',
  '今日を、つづる。',
  '今日を、書き留めて。',
  '今日を、書き残して。',
  '今日を、のこす。',
  'ひと言、書いておく。',
  // ねぎらい・自分への声
  '今日もおつかれさま。',
  '今日もよくやった。',
  'よくやった一日。',
  '今日もいい一日。',
  '今日もありがとう。',
  '自分に、ありがとう。',
  '今日もえらかった。',
  '今日も、よし。',
  // 体の弛緩
  'ひと息、ついて。',
  'ちょっと、ひと休み。',
  '一息ついた、いま。',
  // 温度・温もり
  'あたたかいお茶を、ひと口。',
  'ぽかぽかの夜に。',
  '毛布の中で。',
  '冬の夜長に。',
  // 余韻・心に残ったもの
  '心に残ったものを。',
  '胸にあったものを。',
  'ふと思い出したことを。',
  '印象に残ったことを。',
  '残しておきたいことを。',
  'ちいさな出来事を。',
  '今日のうれしいを、ひと言。',
  '今日の発見を、ひと言。',
  '今日のちいさな喜びを。',
  'ささやかなしあわせを、ひと言。',
  'うれしかったこと、ひとつ。',
  '今日いちばんを、ひと言。',
  // 振り返り
  '今日を、ふりかえる。',
  '今日を、思い返して。',
  '今日のあれこれを。',
  'ぱっと思い浮かぶことを。',
  '真っ先に思い出すことを。',
  // 未来へのまなざし
  '未来の自分へ。',
  '一年後の自分に。',
  '来年の今日に。',
  '思い出のたねに。',
  '明日の自分への、置き手紙。',
  'いつか読み返すための、しるし。',
  'あとで読みたくなる、ひと言。',
  '思い出に、ひとつ。',
  '明日の自分に、ひと言。',
  '5年後の自分に、ひと言。',
  '未来のページに、ひと言。',
] as const;

/**
 * 起動ごとに 1 つだけランダムに選ばれるプロンプト。
 *
 * モジュールスコープで初期化することで、PromptText の再マウントや
 * 画面遷移をまたいでもセッション中は値が固定される。
 * （useMemo([]) だと再マウントで値が変わってしまう）
 */
const PROMPT =
  PROMPT_VARIATIONS[Math.floor(Math.random() * PROMPT_VARIATIONS.length)];

/**
 * 画面上部に常時表示される、書き手をやさしく誘うひと言。
 */
export function PromptText() {
  const c = useColors();
  return (
    <View style={styles.wrapper}>
      <Text style={[styles.prompt, { color: c.ink.muted }]}>{PROMPT}</Text>
    </View>
  );
}

const styles = StyleSheet.create({
  wrapper: {
    paddingHorizontal: spacing.xl,
    paddingTop: spacing.xs,
  },
  prompt: {
    fontFamily: 'NotoSerifJP',
    fontSize: 16,
  },
});
